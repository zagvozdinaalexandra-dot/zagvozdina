// Current language
let currentLang = 'ru';

// Initialize language from localStorage or default to Russian
function initLanguage() {
    const savedLang = localStorage.getItem('cyberaware_lang');
    if (savedLang && (savedLang === 'ru' || savedLang === 'en')) {
        currentLang = savedLang;
    }
    applyTranslations();
    updateLanguageFlags();
}

// Apply translations to the page
function applyTranslations() {
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[currentLang] && translations[currentLang][key]) {
            element.textContent = translations[currentLang][key];
        }
    });

    applyThreatsContent();
    applyTipsContent();
    applyLearningContent();
    
    // Update HTML lang attribute
    document.documentElement.lang = currentLang;
    
    // Save to localStorage
    localStorage.setItem('cyberaware_lang', currentLang);
    
    // Restart typewriter effect for hero subtitle
    restartTypewriter();
}

function applyTipsContent() {
    const tocContainer = document.querySelector('[data-i18n-html="tips_toc_list"]');
    const sectionsContainer = document.querySelector('[data-i18n-html="tips_sections_html"]');

    if (!tocContainer || !sectionsContainer) return;

    if (!tocContainer.dataset.ruOriginal) {
        tocContainer.dataset.ruOriginal = tocContainer.innerHTML;
    }
    if (!sectionsContainer.dataset.ruOriginal) {
        sectionsContainer.dataset.ruOriginal = sectionsContainer.innerHTML;
    }

    if (currentLang === 'en') {
        tocContainer.innerHTML = tipsPageTranslations.en.toc;
        sectionsContainer.innerHTML = tipsPageTranslations.en.sections;
    } else {
        tocContainer.innerHTML = tocContainer.dataset.ruOriginal;
        sectionsContainer.innerHTML = sectionsContainer.dataset.ruOriginal;
    }
}

function applyLearningContent() {
    const sectionsContainer = document.querySelector('[data-i18n-html="learning_sections_html"]');
    if (!sectionsContainer) return;

    if (!sectionsContainer.dataset.ruOriginal) {
        sectionsContainer.dataset.ruOriginal = sectionsContainer.innerHTML;
    }

    if (currentLang !== 'en') {
        sectionsContainer.innerHTML = sectionsContainer.dataset.ruOriginal;
        return;
    }

    const temp = document.createElement('div');
    temp.innerHTML = sectionsContainer.dataset.ruOriginal;

    const sectionsById = learningPageTranslations.en.sectionsById;
    Object.keys(sectionsById).forEach((sectionId) => {
        const target = temp.querySelector(`[id="${sectionId}"]`);
        if (target) {
            target.innerHTML = sectionsById[sectionId];
        }
    });

    sectionsContainer.innerHTML = temp.innerHTML;
}

function applyThreatsContent() {
    const tocContainer = document.querySelector('[data-i18n-html="threats_toc_list"]');
    const sectionsContainer = document.querySelector('[data-i18n-html="threats_sections_html"]');

    if (!tocContainer || !sectionsContainer) return;

    const pageContent = threatsPageTranslations[currentLang] || threatsPageTranslations.ru;
    tocContainer.innerHTML = pageContent.toc;
    sectionsContainer.innerHTML = pageContent.sections;
}

// Restart typewriter effect
function restartTypewriter() {
    const subtitle = document.querySelector('.hero-content h2');
    if (!subtitle) return;
    const text = subtitle.textContent;
    subtitle.textContent = '';
    subtitle.classList.add('typewriter');
    let i = 0;
    function type() {
        if (i < text.length) {
            subtitle.textContent += text.charAt(i);
            i++;
            setTimeout(type, 60);
        }
    }
    type();
}

// Switch language
function switchLanguage(lang) {
    if (lang === 'ru' || lang === 'en') {
        currentLang = lang;
        applyTranslations();
        updateLanguageFlags();
    }
}

// Update language flag active states
function updateLanguageFlags() {
    document.querySelectorAll('.lang-flag').forEach(flag => {
        flag.classList.remove('active');
        if (flag.getAttribute('data-lang') === currentLang) {
            flag.classList.add('active');
        }
    });
}

// Initialize on page load
document.addEventListener('DOMContentLoaded', function() {
    initLanguage();
});
